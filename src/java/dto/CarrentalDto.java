package dto;

public class CarrentalDto {

	private Integer carId;
	private String carPhotoPath;
	private String carName;
	private String companyName;
	private String size;
	private Integer holdPeople;
	private Integer rentalPrice;
	private String carDescription;
	
	public Integer getCarId() {
		return carId;
	}
	public void setCarId(Integer carId) {
		this.carId = carId;
	}
	public String getCarPhotoPath() {
		return carPhotoPath;
	}
	public void setCarPhotoPath(String carPhotoPath) {
		this.carPhotoPath = carPhotoPath;
	}
	public String getCarName() {
		return carName;
	}
	public void setCarName(String carName) {
		this.carName = carName;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public Integer getHoldPeople() {
		return holdPeople;
	}
	public void setHoldPeople(Integer holdPeople) {
		this.holdPeople = holdPeople;
	}
	public Integer getRentalPrice() {
		return rentalPrice;
	}
	public void setRentalPrice(Integer rentalPrice) {
		this.rentalPrice = rentalPrice;
	}
	public String getCarDescription() {
		return carDescription;
	}
	public void setCarDescription(String carDescription) {
		this.carDescription = carDescription;
	}
	

	
}
